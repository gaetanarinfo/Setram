    const $q = useQuasar()

    function showNotif(message, type) {
      $q.notify({
        type: type,
        message: message,
        timeout: 2500,
      })
    }

    $(document).on('click', '#button', function (e) {

      e.preventDefault();

      var email = $('#mail').val(),
        nom = $('#nom').val()

      if (email == "") {
        showNotif('Vous devez entrer votre adresse email', 'negative');
      }

      if (nom == "") {
        showNotif('Vous devez entrer votre nom', 'negative');
      }

      if (email != "" && nom != "") {

        showNotif(' Votre message a bien été envoyé. Nous y répondrons dans les meilleurs délais.', 'positive')

        setTimeout(() => {
          var form = $('#form1');
          form.submit();
